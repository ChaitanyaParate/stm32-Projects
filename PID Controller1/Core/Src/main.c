void integrator() {
    // Accumulate error over time
    integral += error * Ts;
    if (integral < INTEGRAL_MIN) {
            integral = INTEGRAL_MIN;
        }
        else if (integral > 0.75) {
            integral = 0.75;
        }
   }
void filter_derivative(float error, float prev_error) {
    // Apply first-order exponential moving average filter
    //filtered_derivative = ALPHA * derivative + (1 - ALPHA) * previous_derivative;
	//filtered_derivative = derivative*((Ts*ALPHA)/(Ts+ALPHA));
	/*if (error>=0) {
	filtered_derivative = - fabs((2.0 * KD * (error-prev_error)) + (2.0*(tau-Ts)*filtered_derivative) / (2.0 * (tau + Ts)));
	}
	else {
		filtered_derivative = fabs((2.0 * KD * (error-prev_error)) + (2.0*(tau-Ts)*filtered_derivative) / (2.0 * (tau + Ts)));
	}*/

	prev_derivative = filtered_derivative;

	if (error>=0) {
		filtered_derivative = -fabs((error - prev_error)/Ts);
	}
	else {
		filtered_derivative = fabs((error - prev_error)/Ts);
	}
}

float computePID() {
	prop = KP * error;
    inte = KI * integral;
	der = KD * (N * filtered_derivative + (1-N) * prev_derivative);

    if (inte < INTEGRAL_MIN) {
            inte = INTEGRAL_MIN;
        }
        else if (inte > INTEGRAL_MAX ) {
            inte = INTEGRAL_MAX;
        }
	return prop + inte + der;
}
