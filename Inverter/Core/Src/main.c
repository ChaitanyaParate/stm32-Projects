/* USER CODE BEGIN Header_StartDefaultTask */
void Read_Sensors() {

    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    rawVoltage = HAL_ADC_GetValue(&hadc1);
    voltage = rawVoltage * (3.3 / 4096) * VOLTAGE_SCALE;  // Scale to actual voltage
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_2);


    HAL_ADC_Start(&hadc2);
    HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
    rawCurrent = HAL_ADC_GetValue(&hadc2);
    current = rawCurrent * (3.3 / 4096) * CURRENT_SCALE;  // Scale to actual current
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_3);


    HAL_ADC_Start(&hadc3);
    HAL_ADC_PollForConversion(&hadc3, HAL_MAX_DELAY);
    rawTemp = HAL_ADC_GetValue(&hadc3);
    temperature = rawTemp * (3.3 / 4096) * TEMP_SCALE;  // Scale to actual temperature
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_4);
    b++;

}


void Check_Faults() {
    if (voltage > OVER_VOLTAGE_THRESHOLD) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Over Voltage Fault\n", 19, HAL_MAX_DELAY);
        HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);

    } else if (voltage < UNDER_VOLTAGE_THRESHOLD) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Under Voltage Fault\n", 20, HAL_MAX_DELAY);
        HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_15);
    }

    if (current > OVER_CURRENT_THRESHOLD) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Over Current Fault\n", 19, HAL_MAX_DELAY);
        HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_2);
    }

    if (temperature > OVER_TEMPERATURE_THRESHOLD) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Over Temperature Fault\n", 23, HAL_MAX_DELAY);
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_2);
    }
    d++;

}

void Adjust_Output(float desiredOutput) {
    pwmDuty = (float)((desiredOutput / MAX_OUTPUT) * __HAL_TIM_GET_AUTORELOAD(&htim1));


    if (pwmDuty > __HAL_TIM_GET_AUTORELOAD(&htim1)) {
        pwmDuty = __HAL_TIM_GET_AUTORELOAD(&htim1);
        a++;
    }
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pwmDuty);


}

