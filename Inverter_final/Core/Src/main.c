void FDCAN_Transmit(uint32_t *data, uint8_t length) {
    FDCAN_TxHeaderTypeDef txHeader;
    c++;

    // Fill TxHeader
    txHeader.Identifier = 0x123; // Example CAN ID
    txHeader.IdType = FDCAN_STANDARD_ID;
    txHeader.TxFrameType = FDCAN_DATA_FRAME;
    txHeader.DataLength = FDCAN_DLC_BYTES_8;
    txHeader.ErrorStateIndicator = FDCAN_ESI_ACTIVE;
    txHeader.BitRateSwitch = FDCAN_BRS_ON;
    txHeader.FDFormat = FDCAN_FD_CAN;
    txHeader.TxEventFifoControl = FDCAN_NO_TX_EVENTS;
    txHeader.MessageMarker = 0;

    // Copy data into txData (convert to bytes)
    for (uint8_t i = 0; i < length && i < 8; i++) {
        txData[i] = (uint8_t)(data[i] & 0xFF);
    }

    // Transmit
    if (HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan2, &txHeader, (uint8_t *)txData) != HAL_OK) {
        flag = 1;
    }
    e++;
}

